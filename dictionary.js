let RU_TO_EN = new Map();
let EN_TO_RU = new Map();
let RU_WORDS = [];
let EN_WORDS = [];
let currentLevel = 'A1';

function setLevel(level) {
    currentLevel = level;
    const words = DICTIONARY[level] || [];
    RU_WORDS = words.map((w) => w[1]); // Russian (2nd element)
    EN_WORDS = words.map((w) => w[0]); // English (1st element)

    RU_TO_EN.clear();
    EN_TO_RU.clear();
    words.forEach((w) => {
        RU_TO_EN.set(w[1], w[0]); // ru -> en
        EN_TO_RU.set(w[0], w[1]); // en -> ru
    });
}

function getEnTranslation(russianWord) {
    return RU_TO_EN.get(russianWord) || null;
}

function getRuTranslation(englishWord) {
    return EN_TO_RU.get(englishWord) || null;
}

setLevel(currentLevel);
